// 从内存池中取空间给free-list用
template <bool __threads, int __inst>
char*
__default_alloc_template<__threads, __inst>::_S_chunk_alloc(size_t __size, 
                                                            int& __nobjs)
{
    char* __result;
    // 计算需要取出的空间
    size_t __total_bytes = __size * __nobjs;
    // 计算内存池剩余空间
    size_t __bytes_left = _S_end_free - _S_start_free;

    // 剩余空间 >= 需要空间
    if (__bytes_left >= __total_bytes) {
        // 将内存池起始位置作为结果返回，并更新内存池起始位置
        __result = _S_start_free;
        _S_start_free += __total_bytes;
        return(__result);
    // 剩余空间还可以满足1个以上区块的需求
    } else if (__bytes_left >= __size) {
        // 满足能供应的所有区块，更新内存池起始位置
        __nobjs = (int)(__bytes_left/__size);
        __total_bytes = __size * __nobjs;
        __result = _S_start_free;
        _S_start_free += __total_bytes;
        return(__result);
    // 一个区块都满足不了
    } else {
        size_t __bytes_to_get = 
	                2 * __total_bytes + _S_round_up(_S_heap_size >> 4);
        // 以下尝试让内存池的残余零头发挥剩余价值
        if (__bytes_left > 0) {
            // 找到一个合适的free-list，将其分配给它
            _Obj* __STL_VOLATILE* __my_free_list =
                        _S_free_list + _S_freelist_index(__bytes_left);
            // 调整free-list，将残余零头添加进去
            ((_Obj*)_S_start_free) -> _M_free_list_link = *__my_free_list;
            *__my_free_list = (_Obj*)_S_start_free;
        }

        // 配置heap空间，用于补充内存池
        _S_start_free = (char*)malloc(__bytes_to_get);
        // malloc失败
        if (0 == _S_start_free) {
            size_t __i;
            _Obj* __STL_VOLATILE* __my_free_list;
	          _Obj* __p;
            // 尝试从我们“尚有未用且足够大的块”中分配一块
            // 不尝试“尚有未用但小的块”分配，因为在多进程机器上会造成灾难
            for (__i = __size;
                 __i <= (size_t) _MAX_BYTES;
                 __i += (size_t) _ALIGN) {
                __my_free_list = _S_free_list + _S_freelist_index(__i);
                __p = *__my_free_list;
                if (0 != __p) {
                    // 调整free-list，释出未用者块
                    *__my_free_list = __p -> _M_free_list_link;
                    _S_start_free = (char*)__p;
                    _S_end_free = _S_start_free + __i;
                    // 递归调用自己，为了修正nobjs
                    return(_S_chunk_alloc(__size, __nobjs));
                    // 任何剩余零头都将被编入适当的free-list中备用
                }
            }
            // 山穷水尽，没办法了
	          _S_end_free = 0;	// In case of exception.
            // 交给第一级配置器，看看oom还能不能想想办法
            _S_start_free = (char*)malloc_alloc::allocate(__bytes_to_get);
            // 这会导出异常，或者内存不足的情况得到改善
        }
        _S_heap_size += __bytes_to_get;
        _S_end_free = _S_start_free + __bytes_to_get;
        // 递归调用自己，为了修正nobjs
        return(_S_chunk_alloc(__size, __nobjs));
    }
}

// 重新填充函数
// 返回一个__n大小的对象，且可能添加到大小为__n的free-list中
template <bool __threads, int __inst>
void*
__default_alloc_template<__threads, __inst>::_S_refill(size_t __n)
{
    // 调用函数，尝试取得20个大小为__n的区块作为free-list的新节点
    int __nobjs = 20;
    char* __chunk = _S_chunk_alloc(__n, __nobjs);
    _Obj* __STL_VOLATILE* __my_free_list;
    _Obj* __result;
    _Obj* __current_obj;
    _Obj* __next_obj;
    int __i;

    // 如果只得到一个新区块，就将这个区块分配给调用者，无新节点填充
    if (1 == __nobjs) return(__chunk);
    // 否则准备调整free-list，纳入新节点
    __my_free_list = _S_free_list + _S_freelist_index(__n);

    /* 以下将在chunk空间内建立free-list */
    // 这一块准备给客端
    __result = (_Obj*)__chunk;
    // 以下导引free-list指向新配置的空间（取自内存池）
    *__my_free_list = __next_obj = (_Obj*)(__chunk + __n);
    // 以下将free-list的各节点链接起来
    // 从1开始，因为0要给客端
    for (__i = 1; ; __i++) {
      __current_obj = __next_obj;
      __next_obj = (_Obj*)((char*)__next_obj + __n);
      if (__nobjs - 1 == __i) {
          __current_obj -> _M_free_list_link = 0;
          break;
      } else {
          __current_obj -> _M_free_list_link = __next_obj;
      }
    }
    return(__result);
}

// 重分配函数
template <bool threads, int inst>
void*
__default_alloc_template<threads, inst>::reallocate(void* __p,
                                                    size_t __old_sz,
                                                    size_t __new_sz)
{
    void* __result;
    size_t __copy_sz;

    if (__old_sz > (size_t) _MAX_BYTES && __new_sz > (size_t) _MAX_BYTES) {
        return(realloc(__p, __new_sz));
    }
    if (_S_round_up(__old_sz) == _S_round_up(__new_sz)) return(__p);
    __result = allocate(__new_sz);
    __copy_sz = __new_sz > __old_sz? __old_sz : __new_sz;
    memcpy(__result, __p, __copy_sz);
    deallocate(__p, __old_sz);
    return(__result);
}

